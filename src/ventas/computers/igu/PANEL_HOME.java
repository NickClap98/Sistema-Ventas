    private void AgregarCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarCompraActionPerformed
        /*
        Manejar excepciones y traer datos de los productos seleccionados
        y agregarlos en los jlb_txt
         */
        try {
            Double Precio = (Double) TablaProductos.getValueAt(TablaProductos.getSelectedRow(), 3);
            unidades = Integer.parseInt(txt_unidades.getText());
            PrecioFinal = PrecioFinal + Precio * unidades;
            Count = Count + unidades;
            BigDecimal redondear = new BigDecimal(PrecioFinal);
            redondear = redondear.setScale(2, RoundingMode.UP);
            jlb_productosAgregados.setForeground(Color.GREEN);
            jlb_productosAgregados.setText("+ " + Count + " Precio Final: " + redondear);
            txt_unidades.setText("1");
            /*   Si un producto es elegido varias veces que muentre de 1 en 1 los productos
            asi cualquier cosa se elimina de la compra
            con el btn eliminar */

            while (Repetidor != unidades) {
                DefaultTableModel compras = (DefaultTableModel) TablaProductosCompra.getModel();

// Si el modelo no tiene columnas, las agregamos
                if (compras.getColumnCount() == 0) {
                    String titulos[] = {"CODIGO", "NOMBRE", "PRECIO", "ID"};
                    compras.setColumnIdentifiers(titulos);
                }

                int selectedRow = TablaProductos.getSelectedRow();
                if (selectedRow != -1) {
                    Double Price = (Double) TablaProductos.getValueAt(selectedRow, 3);
                    String Nombre = (String) TablaProductos.getValueAt(selectedRow, 1);
                    int Codigo = (int) TablaProductos.getValueAt(selectedRow, 0);
                    int ID = (int) TablaProductos.getValueAt(selectedRow, 5); // Asumiendo que el ID está en la última columna

                    Object[] objeto = {Codigo, Nombre, Price, ID}; // Asegúrate de que los índices coincidan con las columnas
                    compras.addRow(objeto);
                }

                int rowCount = compras.getRowCount();
                int columnCount = compras.getColumnCount();

// Recorrer todas las filas y columnas para extraer los datos 
                for (int i = 0; i < rowCount; i++) {
                    Object[] rowData = new Object[columnCount];
                    for (int j = 0; j < columnCount; j++) {
                        rowData[j] = compras.getValueAt(i, j);
                    }
                    datos.add(rowData);
                }

                Repetidor++;

                TablaProductosCompra.setModel(compras);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Ha ocurrido un error");


    }//GEN-LAST:event_AgregarCompraActionPerformed
        Repetidor = 0;
        cargarTabla();
        ComprobarTextoDeBusqueda(busqueda);
    }
