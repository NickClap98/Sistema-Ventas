    private void btn_busquedaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_busquedaActionPerformed

        try {

            DefaultTableModel TablaDefault = new DefaultTableModel() {
            };

            // nombres de columnas
            String titulos[] = {"CODIGO", "NOMBRE", "STOCK", "PRECIO", "CATEGORIA", "ID"};

            //Setear nombres en coolumnas
            TablaDefault.setColumnIdentifiers(titulos);

// BUSCAR POR NOMBRE O POR CODIGO 
// BUENA PRACTICA RESUMIENDO CODIGO
//*************************************************************
            List<Productos> listaproductos;

            if (busqueda == false) {
                listaproductos = CL.BuscarProductoNombre(txt_producto.getText().toUpperCase());
            } else {
                listaproductos = CL.BuscarProductoCodigo(Integer.parseInt(txt_producto.getText()));
            }
// ********************************************************

//recorrer la lista y mostrar elementos
            if (listaproductos != null) {
                for (Productos product : listaproductos) {
                    Object[] columnas = {product.getCodigo(), product.getNombre(),
                        product.getStock(), product.getPrecio(), product.getCategoria(), product.getId()};
//agregar listas 
                    TablaDefault.addRow(columnas);
                }
            }

            TablaProductos.setModel(TablaDefault);


            /* SI LAS CELDAS(Row) SON MENORES A 1 Y LA BUSQUEDA SE HACE EN NOMBRES
        QUE TIRE UN MENSSAGE DIALOG CORRESPONDIENTE
        SI ES CON CODIGO QUE TIRE OTRO*/
            if (TablaProductos.getRowCount() < 1) {

                if (busqueda == false) {
                    JOptionPane.showMessageDialog(rootPane, "No existe un producto con este Nombre");

                } else {
                    JOptionPane.showMessageDialog(rootPane, "No existe un producto con este Codigo");

                }
                ComprobarTextoDeBusqueda(busqueda);
                cargarTabla();

            }
        } catch (Exception e) {

            JOptionPane.showMessageDialog(rootPane, "Error en el codigo del producto");
            ComprobarTextoDeBusqueda(busqueda);
            cargarTabla();
        }


    }//GEN-LAST:event_btn_busquedaActionPerformed

