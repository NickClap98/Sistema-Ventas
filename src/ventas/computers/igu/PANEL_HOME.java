    public void cargarTabla() {
        DefaultTableModel model1 = new DefaultTableModel() {

            @Override
            //Filas y columnas no son editables
            public boolean isCellEditable(int row, int column) {
                return false;
            }

        };

        // nombres de columnas
        String titulos[] = {"CODIGO", "NOMBRE", "STOCK", "PRECIO", "CATEGORIA", "ID"};

        //Setear nombres en coolumnas
        model1.setColumnIdentifiers(titulos);

        //carga de datos en la db
        List<Productos> listaproductos = CL.cargarTabla();

        //recorrer la lista y mostrar elementos
        if (listaproductos != null) {

            for (Productos product : listaproductos) {
                Object[] objeto = {product.getCodigo(), product.getNombre(),
                    product.getStock(), product.getPrecio(), product.getCategoria(), product.getId()};

//agregar listas 
                model1.addRow(objeto);
            }

        }
        TablaProductos.setModel(model1);
    }

